  methods: {
    fetchEntriesDebounced() {
      clearTimeout(this.timerId);
      this.timerId = setTimeout(() => {
        this.items = this.pokemon;
        this.isLoading = false;
      }, 500);
    },

    //use for infinite scrolling
    scroll() {
      window.onscroll = () => {
        let bottomOfWindow =
          document.documentElement.scrollTop +
            window.innerHeight -
            document.documentElement.offsetHeight <
            100 &&
          document.documentElement.scrollTop +
            window.innerHeight -
            document.documentElement.offsetHeight >=
            -10;
        if (bottomOfWindow) {
          this.fetchPokemon();
        }
      };
    },

    //get pokemon from API
    fetchPokemon: async function() {
      let offset = this.fetchedPokemon.length + 1;
      for (let i = offset; i < offset + 5; i++) {
        // using this wrapper URL which caches pokeapi data
        let response = await fetch(
          `https://fizal.me/pokeapi/api/v2/id/${i}.json`
        );
        let data = await response.json();
        let found =
          this.fetchedPokemon.find(pokemon => pokemon.name === data.name) ===
          undefined
            ? false
            : true;
        if (!found) {
          this.fetchedPokemon.push(data);
        }
      }
    }
